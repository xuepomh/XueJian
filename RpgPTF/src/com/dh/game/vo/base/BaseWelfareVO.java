package com.dh.game.vo.base;

public class BaseWelfareVO {
	private int num;
	private int type;
	private int finished;
	private int rewardid;
	private int vip_levelmin;
	private int vip_levemax;

	public int getNum() {
		return num;
	}

	public void setNum(int num) {
		this.num = num;
	}

	public int getVip_levelmin() {
		return vip_levelmin;
	}

	public void setVip_levelmin(int vip_levelmin) {
		this.vip_levelmin = vip_levelmin;
	}

	public int getVip_levemax() {
		return vip_levemax;
	}

	public void setVip_levemax(int vip_levemax) {
		this.vip_levemax = vip_levemax;
	}

	public int getType() {
		return type;
	}

	public void setType(int type) {
		this.type = type;
	}

	public int getFinished() {
		return finished;
	}

	public void setFinished(int finished) {
		this.finished = finished;
	}

	public int getRewardid() {
		return rewardid;
	}

	public void setRewardid(int rewardid) {
		this.rewardid = rewardid;
	}

}
