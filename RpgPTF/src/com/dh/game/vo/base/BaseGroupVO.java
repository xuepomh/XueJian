package com.dh.game.vo.base;

public class BaseGroupVO {
	private int id;
	private int type;
	private int level;
	private int group;
	private int rewardid;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public int getType() {
		return type;
	}

	public void setType(int type) {
		this.type = type;
	}

	public int getLevel() {
		return level;
	}

	public void setLevel(int level) {
		this.level = level;
	}

	public int getGroup() {
		return group;
	}

	public void setGroup(int group) {
		this.group = group;
	}

	public int getRewardid() {
		return rewardid;
	}

	public void setRewardid(int rewardid) {
		this.rewardid = rewardid;
	}

}
