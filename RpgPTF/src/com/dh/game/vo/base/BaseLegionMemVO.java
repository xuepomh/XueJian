package com.dh.game.vo.base;

public class BaseLegionMemVO {
	private int id;
	private int exp;// 升级所需帮贡
	private int levelreward;// 奖励id

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public int getExp() {
		return exp;
	}

	public void setExp(int exp) {
		this.exp = exp;
	}

	public int getLevelreward() {
		return levelreward;
	}

	public void setLevelreward(int levelreward) {
		this.levelreward = levelreward;
	}
}
