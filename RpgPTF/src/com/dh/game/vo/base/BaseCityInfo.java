package com.dh.game.vo.base;

public class BaseCityInfo {

	private int id;
	private String name;
	private int url;
	private int level;
	private int x;
	private int y;
	private int open_taskid;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getUrl() {
		return url;
	}

	public void setUrl(int url) {
		this.url = url;
	}

	public int getLevel() {
		return level;
	}

	public void setLevel(int level) {
		this.level = level;
	}

	public int getX() {
		return x;
	}

	public void setX(int x) {
		this.x = x;
	}

	public int getY() {
		return y;
	}

	public void setY(int y) {
		this.y = y;
	}

	public int getOpen_taskid() {
		return open_taskid;
	}

	public void setOpen_taskid(int open_taskid) {
		this.open_taskid = open_taskid;
	}

}
