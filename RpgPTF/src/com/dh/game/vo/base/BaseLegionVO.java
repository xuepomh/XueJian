package com.dh.game.vo.base;

public class BaseLegionVO {
	private int id;// 等级
	private int money;
	private int itemid;
	private int number;// 道具数量
	private int members;// 成员数量
	private int deputyleader;// 副帮主数量
	private int chief;// 堂主
	private int dailyreward;// 等级每日奖励

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public int getMoney() {
		return money;
	}

	public void setMoney(int money) {
		this.money = money;
	}

	public int getItemid() {
		return itemid;
	}

	public void setItemid(int itemid) {
		this.itemid = itemid;
	}

	public int getNumber() {
		return number;
	}

	public void setNumber(int number) {
		this.number = number;
	}

	public int getMembers() {
		return members;
	}

	public void setMembers(int members) {
		this.members = members;
	}

	public int getDeputyleader() {
		return deputyleader;
	}

	public void setDeputyleader(int deputyleader) {
		this.deputyleader = deputyleader;
	}

	public int getChief() {
		return chief;
	}

	public void setChief(int chief) {
		this.chief = chief;
	}

	public int getDailyreward() {
		return dailyreward;
	}

	public void setDailyreward(int dailyreward) {
		this.dailyreward = dailyreward;
	}
}
