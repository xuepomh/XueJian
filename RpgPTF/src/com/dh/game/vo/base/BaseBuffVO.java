package com.dh.game.vo.base;

public class BaseBuffVO {

	private int id; // id
	private String name; // 名字
	private int target; // 目标
	private int type; // 效果
	private int num_type; // 数值类型
	private int value; // 数值
	private int last_time; // 持续时间
	private int superposition; // 叠加上限

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getTarget() {
		return target;
	}

	public void setTarget(int target) {
		this.target = target;
	}

	public int getType() {
		return type;
	}

	public void setType(int type) {
		this.type = type;
	}

	public int getNum_type() {
		return num_type;
	}

	public void setNum_type(int num_type) {
		this.num_type = num_type;
	}

	public int getValue() {
		return value;
	}

	public void setValue(int value) {
		this.value = value;
	}

	public int getLast_time() {
		return last_time;
	}

	public void setLast_time(int last_time) {
		this.last_time = last_time;
	}

	public int getSuperposition() {
		return superposition;
	}

	public void setSuperposition(int superposition) {
		this.superposition = superposition;
	}

}
