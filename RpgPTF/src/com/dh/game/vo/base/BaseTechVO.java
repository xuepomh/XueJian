package com.dh.game.vo.base;

/**
 * 基本科技信息 2014年3月24日
 */
public class BaseTechVO {
	private int id;// 流水号
	private String name;// 科技名称
	private int url;// 资源名称
	private int type;// 科技类型
	private int level;// 科技等级
	private int params;// 升级效果参数
	private int nextParams;// 下一级参数
	private int cost;// 消耗游戏币
	private String desc;// 描述
	private int cd;// 不同等级冷却时间

	public int getCd() {
		return cd;
	}

	public void setCd(int cd) {
		this.cd = cd;
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getUrl() {
		return url;
	}

	public void setUrl(int url) {
		this.url = url;
	}

	public int getType() {
		return type;
	}

	public void setType(int type) {
		this.type = type;
	}

	public int getLevel() {
		return level;
	}

	public void setLevel(int level) {
		this.level = level;
	}

	public int getParams() {
		return params;
	}

	public void setParams(int params) {
		this.params = params;
	}

	public int getCost() {
		return cost;
	}

	public void setCost(int cost) {
		this.cost = cost;
	}

	public String getDesc() {
		return desc;
	}

	public void setDesc(String desc) {
		this.desc = desc;
	}

	public int getNextParams() {
		return nextParams;
	}

	public void setNextParams(int nextParams) {
		this.nextParams = nextParams;
	}

}
