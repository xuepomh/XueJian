package com.dh.game.vo.base;

public class BaseBuildLevelVO {
	private int level;
	private int buildid;
	private int reqMoney;
	private int item;
	private int num;
	private int cd;
	private int grid;
	private int times;

	public int getLevel() {
		return level;
	}

	public void setLevel(int level) {
		this.level = level;
	}

	public int getBuildid() {
		return buildid;
	}

	public void setBuildid(int buildid) {
		this.buildid = buildid;
	}

	public int getReqMoney() {
		return reqMoney;
	}

	public void setReqMoney(int reqMoney) {
		this.reqMoney = reqMoney;
	}

	public int getItem() {
		return item;
	}

	public void setItem(int item) {
		this.item = item;
	}

	public int getNum() {
		return num;
	}

	public void setNum(int num) {
		this.num = num;
	}

	public int getCd() {
		return cd;
	}

	public void setCd(int cd) {
		this.cd = cd;
	}

	public int getGrid() {
		return grid;
	}

	public void setGrid(int grid) {
		this.grid = grid;
	}

	public int getTimes() {
		return times;
	}

	public void setTimes(int times) {
		this.times = times;
	}

}
