package com.dh.game.vo.user;

public class StreetEnemyVO {
	private int playerId;
	private int enemyId;
	private String name;
	private int headIcon;
	private int level;
	private boolean isHidden;// 是否使用封山令
	private int atkTime;
	private int isRevenge;// 是否已经复仇

	public int getPlayerId() {
		return playerId;
	}

	public void setPlayerId(int playerId) {
		this.playerId = playerId;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getHeadIcon() {
		return headIcon;
	}

	public void setHeadIcon(int headIcon) {
		this.headIcon = headIcon;
	}

	public int getLevel() {
		return level;
	}

	public void setLevel(int level) {
		this.level = level;
	}

	public boolean isHidden() {
		return isHidden;
	}

	public void setHidden(boolean isHidden) {
		this.isHidden = isHidden;
	}

	public int getEnemyId() {
		return enemyId;
	}

	public void setEnemyId(int enemyId) {
		this.enemyId = enemyId;
	}

	public int getAtkTime() {
		return atkTime;
	}

	public void setAtkTime(int atkTime) {
		this.atkTime = atkTime;
	}

	public int getIsRevenge() {
		return isRevenge;
	}

	public void setIsRevenge(int isRevenge) {
		this.isRevenge = isRevenge;
	}
}
